package com.myDACO.data;

public class Planes {
    String plane;
    String id;

    public Planes(String plane, String id) {
        this.plane = plane;
        this.id = id;
    }

    public String getPlane() {
        return this.plane;
    }

    public String getPlaneId() {
        return this.id;
    }
}
